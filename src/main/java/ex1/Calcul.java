package ex1;

/**
 * Classe qui propose d'effectuer plusieurs types de calcul sur les nombres.
 *
 * @author Someone
 */
public class Calcul {

    /**
     * Calcul la somme de deux entiers
     *
     * @param a le premier entier
     * @param b le second entier
     * @return la somme de deux entiers
     */
    public static int addition(int a, int b) {
        return a + b;
    }


    /**
     * Calcul la différence entre deux entiers
     *
     * @param a le premier entier
     * @param b le second entier
     * @return la différence de deux entiers
     */
    public static int soustraction(int a, int b) {
        return a - b;
    }

}
